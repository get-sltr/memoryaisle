// Get the current/active meal plan (most recent that includes today)
export async function getCurrentMealPlan(householdId: string): Promise<MealPlanWithMeals | null> {
  try {
    const today = new Date().toISOString().split('T')[0];

    const { data, error } = await supabase
      .from('meal_plans')
      .select(`
        *,
        planned_meals (*)
      `)
      .eq('household_id', householdId)
      .lte('start_date', today)
      .gte('end_date', today)
      .order('created_at', { ascending: false })
      .limit(1)
      .single();

    if (error) {
      if (error.code === 'PGRST116') return null; // Not found
      logger.error('Failed to fetch current meal plan:', error);
      throw error;
    }

    return data;
  } catch (error) {
    logger.error('getCurrentMealPlan error:', error);
    return null;
  }
}
