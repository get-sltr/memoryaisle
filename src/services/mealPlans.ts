// Save a meal plan generated by Mira
export async function saveMiraMealPlan(
  householdId: string,
  miraPlan: MiraMealPlan,
  userId?: string
): Promise<MealPlanWithMeals> {
  try {
    // Calculate start and end dates
    const today = new Date();
    const startDate = today.toISOString().split('T')[0];
    const endDate = new Date(today);
    endDate.setDate(today.getDate() + miraPlan.duration - 1);
    const endDateStr = endDate.toISOString().split('T')[0];

    // Create the meal plan
    const { data: mealPlan, error: planError } = await supabase
      .from('meal_plans')
      .insert({
        household_id: householdId,
        name: miraPlan.name || 'Weekly Meal Plan',
        start_date: startDate,
        end_date: endDateStr,
        created_by: userId || null,
      })
      .select()
      .single();

    if (planError) {
      logger.error('Failed to create meal plan:', planError);
      throw planError;
    }

    // Convert Mira's day plans to planned meals
    const plannedMeals: Omit<PlannedMeal, 'id' | 'created_at'>[] = [];

    for (const dayPlan of miraPlan.days) {
      const mealDate = new Date(today);
      mealDate.setDate(today.getDate() + dayPlan.day - 1);
      const dateStr = mealDate.toISOString().split('T')[0];

      // Add breakfast
      if (dayPlan.meals.breakfast) {
        plannedMeals.push(convertMiraMealToPlanned(
          mealPlan.id,
          dateStr,
          'breakfast',
          dayPlan.meals.breakfast
        ));
      }

      // Add lunch
      if (dayPlan.meals.lunch) {
        plannedMeals.push(convertMiraMealToPlanned(
          mealPlan.id,
          dateStr,
          'lunch',
          dayPlan.meals.lunch
        ));
      }

      // Add dinner
      if (dayPlan.meals.dinner) {
        plannedMeals.push(convertMiraMealToPlanned(
          mealPlan.id,
          dateStr,
          'dinner',
          dayPlan.meals.dinner
        ));
      }

      // Add snacks
      if (dayPlan.meals.snacks) {
        plannedMeals.push(convertMiraMealToPlanned(
          mealPlan.id,
          dateStr,
          'snack',
          dayPlan.meals.snacks
        ));
      }
    }

    // Insert all planned meals
    if (plannedMeals.length > 0) {
      const { data: meals, error: mealsError } = await supabase
        .from('planned_meals')
        .insert(plannedMeals)
        .select();

      if (mealsError) {
        logger.error('Failed to create planned meals:', mealsError);
        // Delete the meal plan since we couldn't add meals
        await supabase.from('meal_plans').delete().eq('id', mealPlan.id);
        throw mealsError;
      }

      return {
        ...mealPlan,
        planned_meals: meals || [],
      };
    }

    return {
      ...mealPlan,
      planned_meals: [],
    };
  } catch (error) {
    logger.error('saveMiraMealPlan error:', error);
    throw error;
  }
}

// Helper to convert Mira meal format to database format
function convertMiraMealToPlanned(
  mealPlanId: string,
  date: string,
  mealType: PlannedMeal['meal_type'],
  miraMeal: MiraMeal
): Omit<PlannedMeal, 'id' | 'created_at'> {
  return {
    meal_plan_id: mealPlanId,
    date,
    meal_type: mealType,
    name: miraMeal.name,
    description: miraMeal.description || null,
    calories: miraMeal.calories || null,
    prep_time: miraMeal.prepTime || null,
    ingredients: miraMeal.ingredients || [],
    instructions: [],
    is_completed: false,
    completed_at: null,
  };
}

