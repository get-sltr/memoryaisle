// Update a meal
export async function updateMeal(
  mealId: string,
  updates: Partial<Pick<PlannedMeal, 'name' | 'description' | 'calories' | 'prep_time' | 'ingredients' | 'instructions' | 'is_completed'>>
): Promise<PlannedMeal> {
  try {
    const updateData: Record<string, any> = { ...updates };

    // Add completed_at timestamp if marking as completed
    if (updates.is_completed === true) {
      updateData.completed_at = new Date().toISOString();
    } else if (updates.is_completed === false) {
      updateData.completed_at = null;
    }

    const { data, error } = await supabase
      .from('planned_meals')
      .update(updateData)
      .eq('id', mealId)
      .select()
      .single();

    if (error) {
      logger.error('Failed to update meal:', error);
      throw error;
    }

    return data;
  } catch (error) {
    logger.error('updateMeal error:', error);
    throw error;
  }
}

